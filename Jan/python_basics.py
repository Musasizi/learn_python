# Operations in python
print(1+2)
print(1-2)
print(4/2)
print(4*2)
print(4%2)
print(3%2)
print(0/4)

# Literals in python
# Numeric literals
print(200)
print(10.4)



# String Literals
print('Jane Doe')
print('I may be any sentence')

# Boolean literals
print(True)
print(False)

# Special literals
print(None)

# Collection Literals
print([1, 2, 3])
print(['Jane', 2, 3, False])
print(["UCU", "MUK", "HARVARD", ['Jane', 2, 3, False]])
